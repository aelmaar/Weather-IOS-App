import UIKit

// MARK - Color preferences
extension UIColor {
    static let transparentLightGray: UIColor = {
           return UIColor(red: 0.85, green: 0.85, blue: 0.85, alpha: 0.2)
    }()
    
    static let strokeGray: UIColor = {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.1)
    }()
    
    static let transparentBlackFourty: UIColor = {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.4)
    }()
    
    static let transparentBlackNinty: UIColor = {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.9)
    }()
    
    static let transparentBlackSeventy: UIColor = {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.7)
    }()
}
